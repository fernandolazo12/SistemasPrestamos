    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int fil = jtbListarPersona.getSelectedRow();
        if(fil<0){
            JOptionPane.showMessageDialog(null, "Seleccionar La Persona a MODIFICAR!");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea MODIFICAR la Persona? "); 
            if(JOptionPane.OK_OPTION==confirmar) {
                    int id = Integer.parseInt(txtId.getText());
                    String nombre = txtNombre.getText();
                    String paterno = txtPaterno.getText();
                    String materno = txtMaterno.getText();
                    //String sexo= cboSeleccionar.getSelectedItem().toString();
                    int dni = Integer.parseInt(txtDni.getText());
                    int celular =Integer.parseInt(txtCelular.getText());
                    String direccion = txtDireccion.getText();
                    int x = ad.modificarPersona(id, nombre,paterno,materno ,dni,celular,direccion);
                    if(x==1){
                        JOptionPane.showMessageDialog(null, "Persona MODIFICADO!");
                        updateComponets();
                        limpiar();
                    }else{
                        JOptionPane.showMessageDialog(null, "Persona no se ha MODIFICADO!");      
                    }                   
            }        
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void jtbListarPersonaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbListarPersonaMouseClicked
        // TODO add your handling code here:
         if(evt.getButton()==1){
            int fila = jtbListarPersona.getSelectedRow();
            int celda = (int) jtbListarPersona.getValueAt(fila, 0);
            lista = ad.listarPersona(celda);
            for(int i=0;i<lista.size();i++){
                txtId.setText(""+lista.get(i).getIdPersona());
                txtNombre.setText(lista.get(i).getNombre());
                txtPaterno.setText(lista.get(i).getPaterno());
                txtMaterno.setText(lista.get(i).getMaterno());
                txtDni.setText(String.valueOf(lista.get(i).getDNI()));
                txtCelular.setText(String.valueOf(lista.get(i).getCelular()));
                txtDireccion.setText(lista.get(i).getDireccion());
            }
        }  
    }//GEN-LAST:event_jtbListarPersonaMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
       int fila = jtbListarPersona.getSelectedRow();
        if(fila<0){
            JOptionPane.showMessageDialog(null, "Seleccionar La Persona a ELIMINAR");            
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea ELIMINAR La Persona? "); 
            if(JOptionPane.OK_OPTION==confirmar) {
                    int celda = (int) jtbListarPersona.getValueAt(fila, 0);
                    int x = ad.eliminarPersona(celda);
                    if(x==1){
                        JOptionPane.showMessageDialog(null, "Persona ELIMINADO!");
                        updateComponets();
                        limpiar();
                    }else{
                        JOptionPane.showMessageDialog(null, "Persona no ELIMINADO!");      
                    }                   
            } 
            
        }        
    }//GEN-LAST:event_btnEliminarActionPerformed

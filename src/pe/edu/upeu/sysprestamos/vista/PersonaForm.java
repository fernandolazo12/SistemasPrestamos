    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String paterno = txtPaterno.getText();
        String materno = txtMaterno.getText();
        String sexo= cboSeleccionar.getSelectedItem().toString();
        int dni = Integer.parseInt(txtDni.getText());
        int celular =Integer.parseInt(txtCelular.getText());
        String direccion = txtDireccion.getText();
        op = ad.registrarPersona(nombre,paterno,materno,sexo,dni,celular,direccion);
        limpiar();
        if(op!=0){
            JOptionPane.showMessageDialog(null,"Usuario registrado!");
            updateComponets();            
            btnAgregar.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(null,"Usuario no registrado!");
        } 
        
    }//GEN-LAST:event_btnAgregarActionPerformed


    private void jtbListarInventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbListarInventarioMouseClicked
        // TODO add your handling code here:
        if(evt.getButton()==1){
            int fila = jtbListarInventario.getSelectedRow();
            int celda = (int) jtbListarInventario.getValueAt(fila, 0);
            lista = ad1.listarInventario(celda);
            for(int i=0;i<lista.size();i++){
                txtId.setText(""+lista.get(i).getIdInventario());
                txtEquipo.setText(lista.get(i).getEquipo());
                txtCantidad.setText(""+lista.get(i).getCantidad());             
            }
        }  
    }//GEN-LAST:event_jtbListarInventarioMouseClicked
    void limpiar(){
    txtId.setText(null);
    txtEquipo.setText(null);
    txtCantidad.setText(null);
    }
